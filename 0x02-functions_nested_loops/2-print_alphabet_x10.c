#include <stdio.h>

/**
 * print_alphabet_x10 - Print a-z ten times each on a new line
 * Return: 0
 */
int print_alphabet_x10(void)
{
	int i;

	for (i = 0; i == 10; i++)
	{
		printf("abcdefghijklmnopqrstuvwxyz");
	}
}
/**
 * main - main function
 * Return: 0
 */
int main(void)
{
	print_alphabet_x10();
	return (0);
}